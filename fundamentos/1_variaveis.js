// Declaração de variáveis usando var (menos recomendado atualmente)
var idade = 25;

// Declaração de variáveis usando let (recomendado para variáveis que vão mudar de valor)
let nome = "João";

// Declaração de constante usando const (valor não pode ser alterado)
const PI = 3.14159;

console.log(idade)
console.log(nome)
console.log(PI)